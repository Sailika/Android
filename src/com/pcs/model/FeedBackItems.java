package com.pcs.model;

public class FeedBackItems {
	private int image;
	private int count;

	public FeedBackItems(int image, int count) {
		this.image = image;
		this.count = count;

	}
	public int getImage() {
		return image;
	}

	public void setImage(int image) {
		this.image = image;
	}




	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
